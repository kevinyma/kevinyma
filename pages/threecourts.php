						The House with Three Courts is one such exploration, and tremendously interesting one to boot. All walls except those of the inner rooms are made of glass.
						Thus, the interior of the house is maximally dematerialized; the house is nearly pure membrane. Yet, the courtyard wall that encloses the 
						entire structure acts as a tough exoskeleton around the membrane. Notions of public and private space are both adhered to and subverted completely.

